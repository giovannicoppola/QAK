#!/usr/bin/env python3
"""
Phase 9c: QAK Summary Note Generator
Reads qak.db and generates auto-updated QAK summary notes in the vault.

Summary notes are prefixed with "QAK — " and overwritten entirely each run.
"""

import sqlite3
from pathlib import Path
from datetime import datetime

DB_PATH = Path("/Users/giovanni.coppola/Library/CloudStorage/OneDrive-RegeneronPharmaceuticals,Inc/MyScripts/myGitHubRepos/QAK/qak.db")
VAULT = Path("/Users/giovanni.coppola/Library/CloudStorage/OneDrive-RegeneronPharmaceuticals,Inc/MyScripts/myGitHubRepos/gitVault/gitVault-notes")

NOW = datetime.now().strftime("%Y-%m-%d")
NOW_ISO = datetime.now().isoformat(timespec="seconds")
GENERATED_BY = "QAK builder v1.0"


def fmt(val, default="—"):
    """Format a value for table display."""
    if val is None or val == "" or val == "None":
        return default
    if isinstance(val, float) and val == int(val):
        return f"{int(val):,}"
    if isinstance(val, (int, float)):
        return f"{val:,}" if val >= 1000 else str(val)
    return str(val)


def link(name, prefix=""):
    """Create an Obsidian wiki-link."""
    if not name:
        return "—"
    return f"[[{prefix}{name}]]"


def frontmatter(source_query):
    """Generate YAML frontmatter for a summary note."""
    return f"""---
type: qak_summary
last_updated: "{NOW_ISO}"
source_query: "{source_query}"
generated_by: "{GENERATED_BY}"
tags:
  - qak-summary
  - auto-generated
---"""


def banner(title):
    """Generate the banner line."""
    return f"""\n# {title}\n> Auto-generated by QAK builder. Last updated: {NOW}. Do not edit manually.\n"""


def write_note(filename, content):
    """Write a summary note to the vault."""
    path = VAULT / filename
    path.write_text(content, encoding="utf-8")
    return path


